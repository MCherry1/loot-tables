/**
 * Extract data from Excel files into TypeScript source files.
 * Run with: npm run extract
 */
import XLSX from 'xlsx';
import { writeFileSync, mkdirSync } from 'fs';
import { join } from 'path';

const DATA_DIR = join(import.meta.dirname, '..', 'src', 'data');
mkdirSync(DATA_DIR, { recursive: true });

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function readWorkbook(name: string) {
  return XLSX.readFile(join(import.meta.dirname, '..', 'data', name));
}

/** Read a table that spans 5 columns starting at `startCol` (0-based). */
function readTable(
  ws: XLSX.WorkSheet,
  startCol: number,
): { name: string; entries: { name: string; source: string; weight: number }[] } | null {
  const range = XLSX.utils.decode_range(ws['!ref']!);
  const headerCell = ws[XLSX.utils.encode_cell({ r: 0, c: startCol })];
  if (!headerCell || !headerCell.v) return null;

  const name = String(headerCell.v);
  const entries: { name: string; source: string; weight: number }[] = [];

  for (let r = 1; r <= range.e.r; r++) {
    const nameCell = ws[XLSX.utils.encode_cell({ r, c: startCol })];
    const srcCell = ws[XLSX.utils.encode_cell({ r, c: startCol + 1 })];
    const weightCell = ws[XLSX.utils.encode_cell({ r, c: startCol + 2 })];
    if (!nameCell || !nameCell.v) continue;
    const weight = weightCell ? Number(weightCell.v) : 0;
    if (weight <= 0) continue;
    entries.push({
      name: String(nameCell.v).trim(),
      source: srcCell ? String(srcCell.v).trim() : '',
      weight,
    });
  }
  return { name, entries };
}

/** Read all tables on a sheet (spaced every `colStep` columns). */
function readAllTables(ws: XLSX.WorkSheet, colStep = 5) {
  const range = XLSX.utils.decode_range(ws['!ref']!);
  const tables: ReturnType<typeof readTable>[] = [];
  for (let c = 0; c <= range.e.c; c += colStep) {
    const t = readTable(ws, c);
    if (t && t.entries.length > 0) tables.push(t);
  }
  return tables.filter(Boolean) as NonNullable<ReturnType<typeof readTable>>[];
}

function writeTS(filename: string, varName: string, data: unknown, typeAnnotation?: string) {
  const json = JSON.stringify(data, null, 2);
  const annotation = typeAnnotation ? `: ${typeAnnotation}` : '';
  const content = `// Auto-generated by scripts/extract-data.ts — do not edit\n\nexport const ${varName}${annotation} = ${json} as const;\n`;
  writeFileSync(join(DATA_DIR, filename), content);
  console.log(`  wrote ${filename}`);
}

