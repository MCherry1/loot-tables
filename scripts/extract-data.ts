// --- Custom Gems (New Gems) ---
console.log('Extracting Custom Gems...');
{
  const ws = lootWB.Sheets['New Gems'];
  const range = XLSX.utils.decode_range(ws['!ref']!);

  // Metadata in columns A-D (rows 1+)
  const gemMeta: { rarity: string; baseValue: number; tableName: string }[] = [];
  for (let r = 1; r <= range.e.r; r++) {
    const rarity = ws[XLSX.utils.encode_cell({ r, c: 0 })]?.v;
    const baseVal = ws[XLSX.utils.encode_cell({ r, c: 1 })]?.v;
    const tableName = ws[XLSX.utils.encode_cell({ r, c: 4 })]?.v;
    if (!rarity && !tableName) break;
    gemMeta.push({
      rarity: String(rarity || ''),
      baseValue: Number(baseVal || 0),
      tableName: String(tableName || ''),
    });
  }

  // Tables start at column F (index 5), every 5 columns
  // But the layout is: col5=baseVal, col6=tableName, col7=weight, col8=dieRange, col9=import
  // Actually the structure is irregular — let me read more carefully
  // Col E(4)=TableName header row, then col F(5) onward has base, name, weight, d100, import
  // The tables start at column 6 (G), stepping by 5: cols 6,11,16,21,26,31,36,41
  const gemTables: { name: string; baseValue: number; entries: { name: string; weight: number }[] }[] = [];
  for (let startC = 6; startC <= range.e.c; startC += 5) {
    const nameCell = ws[XLSX.utils.encode_cell({ r: 0, c: startC })];
    if (!nameCell?.v) continue;
    const tableName = String(nameCell.v);
    // Find the base value from the column before (startC - 1)
    const baseCell = ws[XLSX.utils.encode_cell({ r: 0, c: startC - 1 })];
    const baseValue = baseCell ? Number(baseCell.v) : 0;

    const entries: { name: string; weight: number }[] = [];
    for (let r = 1; r <= range.e.r; r++) {
      const entryName = ws[XLSX.utils.encode_cell({ r, c: startC })]?.v;
      const weight = ws[XLSX.utils.encode_cell({ r, c: startC + 1 })]?.v;
      if (!entryName) continue;
      const w = Number(weight || 0);
      if (w <= 0) continue;
      entries.push({ name: String(entryName).trim(), weight: w });
    }
    if (entries.length > 0) {
      gemTables.push({ name: tableName, baseValue, entries });
    }
  }
  writeTS('gems.ts', 'CUSTOM_GEMS', gemTables);
  writeTS('gem-meta.ts', 'GEM_META', gemMeta);
}

