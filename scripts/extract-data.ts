const itemWB = readWorkbook('Item Tables.xlsx');

// --- Magic Item Tables (all expanded sheets) ---
console.log('Extracting Magic Item Tables...');
{
  const sheetNames = [
    'DMG Magic Item Tables',
    'Table A Common',
    'Table B Minor Uncommon',
    'Table F Major Uncommon',
    'Table C Minor Rare',
    'Table G Major Rare',
    'Table D Minor Very Rare',
    'Table H Major Very Rare',
    'Table E Minor Legendary',
    'Table I Major Legendary',
  ];

  const allTables: Record<string, { name: string; source: string; weight: number }[]> = {};

  for (const sheetName of sheetNames) {
    const ws = itemWB.Sheets[sheetName];
    if (!ws) { console.warn(`  sheet "${sheetName}" not found, skipping`); continue; }
    const tables = readAllTables(ws);
    for (const t of tables) {
      allTables[t.name] = t.entries;
    }
  }
  writeTS('magic-items.ts', 'MAGIC_ITEMS', allTables);
}

// --- Spells ---
console.log('Extracting Spells...');
{
  const ws = itemWB.Sheets['All Spells'];
  const tables = readAllTables(ws);
  writeTS('spells.ts', 'SPELL_TABLES', tables);
}

// --- Supplemental Types ---
console.log('Extracting Supplemental Types...');
{
  const ws = itemWB.Sheets['Supplemental Types'];
  // This sheet has a Table-of-Contents in col A, then tables starting at col B
  // Tables are spaced every 5 cols but start at col 1 (B)
  const range = XLSX.utils.decode_range(ws['!ref']!);
  const tables: { name: string; entries: { name: string; source: string; weight: number }[] }[] = [];

  for (let c = 1; c <= range.e.c; c += 5) {
    const t = readTable(ws, c);
    if (t && t.entries.length > 0) tables.push(t);
  }
  writeTS('supplemental.ts', 'SUPPLEMENTAL_TABLES', tables);
}

// --- Sourcebooks ---
console.log('Extracting Sourcebooks...');
{
  const ws = itemWB.Sheets['Cover Sheet'];
  const range = XLSX.utils.decode_range(ws['!ref']!);
  const sourcebooks: { name: string; acronym: string }[] = [];

  // Sourcebook list starts at row 12 (0-indexed=11), columns A-B
  for (let r = 11; r <= range.e.r; r++) {
    const nameCell = ws[XLSX.utils.encode_cell({ r, c: 0 })];
    const acroCell = ws[XLSX.utils.encode_cell({ r, c: 1 })];
    if (!nameCell?.v || !acroCell?.v) continue;
    const name = String(nameCell.v).trim();
    const acronym = String(acroCell.v).trim();
    if (name && acronym && acronym.length <= 6) {
      sourcebooks.push({ name, acronym });
    }
  }
  writeTS('sourcebooks.ts', 'SOURCEBOOKS', sourcebooks);
}

console.log('\nDone! All data extracted to src/data/');
